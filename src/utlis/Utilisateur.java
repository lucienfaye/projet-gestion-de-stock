    public void table() {
        List<User> listUtiliateur = new ArrayList<>();
        try {
            String a[] = {"id", "id_user", "username", "password", "type"};
            rs = db.querySelectAll( "utilisateur");
            while(rs.next()){
                User u = new User();
                u.setId(rs.getInt("id"));
                u.setIdUser(rs.getInt("id_user"));
                u.setUsername(rs.getString("username"));
                u.setPassword(rs.getString("password"));
                u.setType(rs.getString("type"));
                listUtiliateur.add(u);
            }
            UtilsFonction.displayDataInTable(listUtiliateur, table_user, List.of("id"));
           // table_user.setModel(new ResultSetTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(Utilisateur.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

